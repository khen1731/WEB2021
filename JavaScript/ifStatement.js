const id = 100;

// Equal to ==
if(id == 100) {
console.log('Correct');
} else {
    console.log('Incorrect');
}

// Not equal !=
if(id != 100) {
    console.log('Correct');
} else {
    console.log('Incorrect');
}

// And equal to value & type ===
if(id === '100'){
    console.log('Correct');
} else {
    console.log('Incorrect');
}

// Not equal to value & type !==
if(id !== '100'){
    console.log('Correct');
} else {
    console.log('Incorrect');
}

